# Strings(str)
# sequences of characters and can be used to store text of all kinds.
# can concatenate strings to create a new string using the plus sign
# can also access individual characters using square brackets and an index
# name of class for strings is 'str'
# can turn other objects into strings

s = "Hello"
t = "World."
u = s + t
print(type(u))
print(u)
print(u[9])

n = str(1234)
print(n[2])
